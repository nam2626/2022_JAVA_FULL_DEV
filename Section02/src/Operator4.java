
public class Operator4 {
	/*
	 * 	관계연산자(비교연산자) ---> 부등식
	 * 		
	 * 		>	<	>=	<=	==	!=
	 * 
	 */
	public static void main(String[] args) {
		int n1 = 10, n2 = 7;
		
		System.out.println(n1 > n2);
		System.out.println(n1 < n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 <= n2);
		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		
		boolean flag = n1 >= 300;
		System.out.println(flag);
	}

}








